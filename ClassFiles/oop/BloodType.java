package oop;
/**
 * The {@code BloodType} enum represents the different human blood types based on the ABO and RhD classification systems that
 * our patients in the hospital can have. It includes the eight major blood types.
 * @author Ryan Ching
 * @version 1.0
 * @since 2024-11-09
 */
public enum BloodType{
    /** Represents blood type O-negative. */
    OMinus,
    /** Represents blood type O-positive. */
    OPlus,
    /** Represents blood type A-negative. */
    AMinus,
    /** Represents blood type A-positive. */
    APlus,
    /** Represents blood type B-negative. */
    BMinus,
    /** Represents blood type B-positive. */
    BPlus,
    /** Represents blood type AB-negative. */
    ABMinus, 
    /** Represents blood type AB-positive. */
    ABPlus
}